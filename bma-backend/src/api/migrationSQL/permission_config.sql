CREATE TABLE BMA_PHASE_II.MENUS (
	id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	name varchar2(60) NOT NULL,
	path varchar2(60) DEFAULT NULL,
	child_obj varchar2(2000),
	for_super_user NUMBER(1,0) DEFAULT 0 NOT NULL,
	CONSTRAINT child_obj_json
		CHECK (child_obj IS JSON)
);

CREATE TABLE BMA_PHASE_II.PERMISSIONS (
	id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	user_level_id NUMBER NOT NULL,
	menu_id NUMBER NOT NULL,
	CONSTRAINT fk_user_level_id_permission
	FOREIGN KEY (USER_LEVEL_ID)
	REFERENCES BMA_PHASE_II.USER_LEVELS(id)
	ON DELETE CASCADE,
	CONSTRAINT fk_menu_id_permission
		FOREIGN KEY (menu_id)
		REFERENCES BMA_PHASE_II.MENUS(id)
		ON DELETE CASCADE
);