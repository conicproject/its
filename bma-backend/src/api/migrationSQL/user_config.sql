CREATE TABLE BMA_PHASE_II.USERS (
	id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	group_id NUMBER NOT NULL,
	name varchar2(60) NOT NULL,
	password varchar2(150) NOT NULL,
	is_super_user NUMBER(1,0) DEFAULT 0 NOT NULL,
	CONSTRAINT unique_user_name UNIQUE(name),
	CONSTRAINT fk_group_id_user
		FOREIGN KEY (group_id)
		REFERENCES BMA_PHASE_II.GROUPS(id)
		ON DELETE CASCADE
);

CREATE TABLE BMA_PHASE_II.LOGINS (
	id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	user_id NUMBER NOT NULL,
	created_at TIMESTAMP NOT NULL,
	expired_at TIMESTAMP NOT NULL,
	CONSTRAINT fk_user_id_login
		FOREIGN KEY (user_id)
		REFERENCES BMA_PHASE_II.USERS(id)
		ON DELETE CASCADE
);