CREATE TABLE BMA_PHASE_II.VIOLATION_RECORDS (
	id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	checkpoint_id NUMBER NOT NULL,
	direction varchar2(10) NOT NULL,
	car_type_id NUMBER NOT NULL,
	volume NUMBER NOT NULL,
	time_range_id NUMBER NOT NULL,
	created_date DATE NOT NULL,
	created_at TIMESTAMP NOT NULL,
	CONSTRAINT fk_time_range_id_violation_record
		FOREIGN KEY (time_range_id)
		REFERENCES BMA_PHASE_II.TIME_RANGES(id)
		ON DELETE CASCADE
);