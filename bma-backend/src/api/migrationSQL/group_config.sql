CREATE TABLE BMA_PHASE_II.DEPARTMENTS (
	id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	name varchar2(100) NOT NULL,
	detail varchar2(500) DEFAULT NULL,
	CONSTRAINT unique_department_name UNIQUE(name)
);

CREATE TABLE BMA_PHASE_II.USER_LEVELS (
	id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	name varchar2(100) NOT NULL,
	detail varchar2(500) DEFAULT NULL,
	CONSTRAINT unique_user_level_name UNIQUE(name)
);

CREATE TABLE BMA_PHASE_II.GROUPS (
	id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	department_id NUMBER NOT NULL,
	user_level_id NUMBER NOT NULL,
	description varchar2(500) DEFAULT NULL,
	CONSTRAINT fk_department_id_group
		FOREIGN KEY (department_id)
		REFERENCES BMA_PHASE_II.DEPARTMENTS(id)
		ON DELETE CASCADE,
	CONSTRAINT fk_user_level_id_group
		FOREIGN KEY (user_level_id)
		REFERENCES BMA_PHASE_II.USER_LEVELS(id)
		ON DELETE CASCADE
);
